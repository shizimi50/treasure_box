class FavoriteItem < ApplicationRecord
    # =====Validation======
    validates :favorite_id, :name, :type, :order, presence: true #空の値に対してバリデーション設定
    # ＝＝＝＝＝＝＝＝＝＝＝＝＝

    # ＝＝＝＝関連付け＝＝＝＝＝
    belongs_to :favorite #userとの関連付け
    has_one :favorite_value #favorite_valueとの関連付け
    # ＝＝＝＝＝＝＝＝＝＝＝＝＝

    # ＝＝フレームワーク関連＝＝
    acts_as_paranoid #論理削除適用
    # ＝＝＝＝＝＝＝＝＝＝＝＝＝

  end
